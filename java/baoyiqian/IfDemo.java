class IfDemo 
{
	public static void main(String[] args) 
	{
		int x=3;
		if (x>1)
		{System.out.println("yes");
		}
		else
		{System.out.println("no");
		}
		/*
		if else结构简写格式： 变量=（条件表达式）?表达式1：表达式2；
		三元运算符：
		好处:可以简化if else代码。
		弊端：因为是一个运算符，所以运算完必须要有一个结果。
		*/
		System.out.println("Hello World!");
	}
}
